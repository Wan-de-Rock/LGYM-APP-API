namespace LgymApp.Domain.Enums;

/// <summary>
/// Represents the components of a date and time.
/// </summary>
public enum DateTimeComponentsEnum
{
    /// <summary>
    /// The year component of a date.
    /// </summary>
    Year,

    /// <summary>
    /// The month component of a date.
    /// </summary>
    Month,

    /// <summary>
    /// The day component of a date.
    /// </summary>
    Day,

    /// <summary>
    /// The hour component of a time.
    /// </summary>
    Hour,

    /// <summary>
    /// The minute component of a time.
    /// </summary>
    Minute,

    /// <summary>
    /// The second component of a time.
    /// </summary>
    Second,

    /// <summary>
    /// The millisecond component of a time.
    /// </summary>
    Millisecond
}
